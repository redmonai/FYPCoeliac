public class Product {
    public String name;
    public String brand;
    public String category;
    public String type;
    public String size;

    public Product ()
    {

    }

    public Product (String name, String brand, String category, String type, String size)
    {
        this.name = name;
        this.brand = brand;
        this.category = category;
        this.type = type;
        this.size = size;
    }
}

